package Model;

public class Model {
	private int temp;
	private int hum;
	private double pdr;
	private int con;
	/**
	 * @return the temp
	 */
	public int getTemp() {
		return temp;
	}
	/**
	 * @param temp the temp to set
	 */
	public void setTemp(int temp) {
		this.temp = temp;
	}
	/**
	 * @return the hum
	 */
	public int getHum() {
		return hum;
	}
	/**
	 * @param hum the hum to set
	 */
	public void setHum(int hum) {
		this.hum = hum;
	}
	/**
	 * @return the pdr
	 */
	public double getPdr() {
		return pdr;
	}
	/**
	 * @param pdr the pdr to set
	 */
	public void setPdr(double pdr) {
		this.pdr = pdr;
	}
	/**
	 * @return the con
	 */
	public int getCon() {
		return con;
	}
	/**
	 * @param con the con to set
	 */
	public void setCon(int con) {
		this.con = con;
	}
}
